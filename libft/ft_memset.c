#include <strings.h>

void	*ft_memset(void *b, int c, size_t len)
{
	unsigned char *d;
	unsigned char p;

	d = b;
	p = c;
	while (len > 0)
	{
		*d = p;
		d++;
		len--;
	}
	return (b);
}
